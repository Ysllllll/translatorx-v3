_ops = TextOps.for_language("ko")


def _s(text: str) -> list[str]:
    return Span.to_texts(split_sentences(
        text, _ops.sentence_terminators, _ops.abbreviations, is_cjk=False,
    ))


class TestKoreanSplitter(SplitterTestBase):
    LANGUAGE = "ko"
    TEXT_SAMPLE = TEXT_SAMPLE
    PARAGRAPH_TEXT = PARAGRAPH_TEXT

    # ── split_sentences() ─────────────────────────────────────────────

    def test_split_sentences(self) -> None:
        assert _s("안녕하세요. 반갑습니다!") == ["안녕하세요.", " 반갑습니다!"]

    def test_split_sentences_ops_shortcut(self) -> None:
        assert Span.to_texts(_ops.split_sentences("안녕하세요. 반갑습니다!")) == ["안녕하세요.", " 반갑습니다!"]

